    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
            
        
         if (Util.perguntar("Deseja excluir o registro") == true) {

            ProdutoKvm produto = viewBean();
            Produto_DAO produtoDAO = new Produto_DAO();//executa o dao
            produtoDAO.delete(produto);

        } else {

            Util.mensagem("ExclusÃ£o cancelada.");
        }
        
        
        
        Util.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,jTxtCodigoKvm,jTxtMarcaKvm,jTxtModeloKvm,jTxtTamanhoKvm,jTxtModeloKvm,jTxtPrecoKvm);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed

               
          ProdutoKvm produto = viewBean();
        Produto_DAO produtoDAO = new Produto_DAO();//executa o dao
        //boolean pra incluir ou alterar
        if(incl == true){
        produtoDAO.insert(produto);
        
        }else{
        produtoDAO.upedate(produto);
        }



        Util.habilitar(false, jTxtCodigoKvm,jTxtMarcaKvm,jTxtModeloKvm,jTxtTamanhoKvm,jTxtModeloKvm,jTxtPrecoKvm);
        Util.habilitar(true, jBtnIncluir,jBtnPesquisar);
        Util.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,jTxtCodigoKvm,jTxtMarcaKvm,jTxtModeloKvm,jTxtTamanhoKvm,jTxtModeloKvm,jTxtPrecoKvm);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

