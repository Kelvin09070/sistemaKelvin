     public ProdutoKvm viewBean() {
        ProdutoKvm produto = new ProdutoKvm();
        produto.setIdprodutoKvm(Util.strInt(jTxtCodigoKvm.getText()));
        produto.setMarcaKvm(jTxtMarcaKvm.getText());
        produto.setModeloKvm(jTxtModeloKvm.getText());       
        produto.setCorKvm(jTxtModeloKvm.getText());
        produto.setPrecoKvm(Util.strDouble(jTxtPrecoKvm.getText()));
              

        return produto;
    }
    
    
        public ProdutoKvm beanView(ProdutoKvm produto){
            
        jTxtCodigoKvm.setText(Util.intStr(produto.getIdprodutoKvm()));
        jTxtMarcaKvm.setText(produto.getMarcaKvm());
        jTxtModeloKvm.setText(produto.getModeloKvm());
        jTxtModeloKvm.setText(produto.getCorKvm());
        jTxtPrecoKvm.setText(Util.doubleStr(produto.getPrecoKvm()));
          
        jBtnAlterar.setEnabled(true);
        return produto;
        }        


