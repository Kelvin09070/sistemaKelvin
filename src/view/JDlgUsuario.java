     public UsuarioKvm viewBean() {
        UsuarioKvm usuario = new UsuarioKvm();
        
        
        usuario.setIdusuarioKvm(Util.strInt(JTxtCodigoMsl.getText()));
        usuario.setNomeKvm(JTxtNomeKvm.getText());
        
        usuario.setApelidoKvm(jTxtApelidoKvm.getText());
        usuario.setCpfKvm(jFmtCpfKvm.getText());
        
        usuario.setEmailKvm(JTxtEmailKvm.getText());
        usuario.setSenhaKvm(jPwfSenhaKvm.getText());
        

        return usuario;
    }
    
    
        public UsuarioKvm beanView(UsuarioKvm usuario){
            
        JTxtCodigoMsl.setText(Util.intStr(usuario.getIdusuarioKvm()));
        
        JTxtNomeKvm.setText(usuario.getNomeKvm());
        
        
        
        jTxtApelidoKvm.setText(usuario.getApelidoKvm());
        jFmtCpfKvm.setText(usuario.getCpfKvm());
        JTxtEmailKvm.setText(usuario.getEmailKvm());
       
       
        jPwfSenhaKvm.setText(usuario.getSenhaKvm());
        
      
        jBtnAlterar.setEnabled(true);
        return usuario;
        }        


