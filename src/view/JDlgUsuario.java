    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
            
        
         if (Util.perguntar("Deseja excluir o registro") == true) {

            UsuarioKvm usuario = viewBean();
            Usuario_DAO usuarioDAO = new Usuario_DAO();//executa o dao
            usuarioDAO.delete(usuario);

        } else {

            Util.mensagem("Exclusão cancelada.");
        }
        
        
        
        Util.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,JTxtCodigoMsl,jFmtCpfKvm,JTxtNomeKvm,JTxtEmailKvm,jPwfSenhaKvm);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed

               
          UsuarioKvm usuario = viewBean();
        Usuario_DAO usuarioDAO = new Usuario_DAO();//executa o dao
        //boolean pra incluir ou alterar
        if(incl == true){
        usuarioDAO.insert(usuario);
        
        }else{
        usuarioDAO.upedate(usuario);
        }



        Util.habilitar(false, JTxtCodigoMsl,jFmtCpfKvm,JTxtNomeKvm,JTxtEmailKvm,jPwfSenhaKvm);
        Util.habilitar(true, jBtnIncluir,jBtnPesquisar);
        Util.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,JTxtCodigoMsl,jFmtCpfKvm,JTxtNomeKvm,JTxtEmailKvm,jPwfSenhaKvm);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

