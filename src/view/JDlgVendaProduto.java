    private void jTxtQuantidadeKvmKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtQuantidadeKvmKeyReleased
    if (jTxtQuantidadeKvm.getText().isEmpty() == false) { // Verifica se o não está vazio
    double unitario = Util.strDouble(jTxtValorUnitarioKvm.getText()); // Converte de texto para um double
    int quantidade = Util.strInt(jTxtQuantidadeKvm.getText()); // Converte de texto para um inteiro
    jTxtTotalKvm.setText(String.valueOf(quantidade * unitario)); 
} else {
    jTxtTotalKvm.setText("0"); // Se o campo de quantidade estiver vazio, define o total como "0"
}

    }//GEN-LAST:event_jTxtQuantidadeKvmKeyReleased

