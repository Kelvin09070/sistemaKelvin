    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        VendaProdutoKvm vendaProdutoKvm = new VendaProdutoKvm();// Criação de um novo objeto VendaProdutoKvm para representar um produto vendido
        VendaProdutoControle vendas = new VendaProdutoControle();// Criação de um controle de vendas de produtos
        vendaProdutoKvm.setProdutoKvm((ProdutoKvm) jCboProdutoKvm.getSelectedItem());// Define o produto selecionado no ComboBox na instância de VendaProdutoKvm
        vendaProdutoKvm.setQuantidadeKvm(Util.strInt(jTxtQuantidadeKvm.getText()));// Converte o valor da quantidade (String) do campo de texto para inteiro e define a quantidade do produto vendido
        vendaProdutoKvm.setValorUnitKvm((int) Util.strDouble(jTxtValorUnitarioKvm.getText()));// Converte o valor unitário do produto (String) para double e o define como valor unitário na instância de VendaProdutoKvm
        if (getTitle().toUpperCase().substring(0, 1).equals("I")) {// Verifica se o título da janela (getTitle()) começa com a letra "I" (indicando que está inserindo um novo item)
            jDlgVenda.vendaProdutoControle.addBean(vendaProdutoKvm);    // Adiciona o novo produto à lista de produtos controlada pelo diálogo de vendas
        } else {// Atualiza o produto selecionado na lista com os novos valores
            jDlgVenda.vendaProdutoControle.updateBean(jDlgVenda.getSelectedRowProd(), vendaProdutoKvm);
        }


        Util.limparCampos(jTxtQuantidadeKvm);// Limpa o campo de quantidade após a inserção ou atualização do produto


        setVisible(false);// Fecha a janela de diálogo, tornando-a invisível

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

